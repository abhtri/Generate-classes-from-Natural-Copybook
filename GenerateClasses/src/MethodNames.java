
public class MethodNames {

	private String methodVariable,MethodLength;
	private int level; 

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getMethodVariable() {
		return methodVariable;
	}

	public void setMethodVariable(String methodVariable) {
		this.methodVariable = methodVariable;
	}

	public String getMethodLength() {
		return MethodLength;
	}

	public void setMethodLength(String methodLength) {
		MethodLength = methodLength;
	}
	
}
